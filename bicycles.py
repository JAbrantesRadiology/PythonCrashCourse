bicycles = ["trek", "merida" , "cannondale", "pinarelo"]
print(bicycles)
#Indexado a 0 (ou seja o primeiro elemento da lista é o 0)
print(bicycles[0])
print(bicycles[0].title())
#para acessar o ultimo elemento da lista, colocar o -1
print(bicycles[-1])
#para usar um elemento da lista numa frase , utilizar o comando f""
print(f"As melhores bicicletas são as {bicycles[0].upper()}.")

